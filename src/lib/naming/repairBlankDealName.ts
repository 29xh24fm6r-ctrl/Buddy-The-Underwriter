/**
 * SPEC-BUDDY-HARD-STOP-AUDIT-AND-RECOVERY-1 #7:
 * Final fallback that ensures no deal completes intake processing with a
 * blank, "Untitled deal", or "NEEDS NAME" name. Runs AFTER
 * runNamingDerivation() — by that point any classifier/anchor-doc-based
 * derivation has already attempted to set the name. This helper exists
 * solely to repair the residual case where derivation produced no usable
 * name and the deal would otherwise show up in pipelines as "NEEDS NAME".
 *
 * Source order:
 *   1. borrower_name on the deal row (the value bookmarked at intake
 *      bootstrap, which itself comes from the borrower-portal or
 *      borrower-applicant record).
 *   2. The deal's primary owner full_name (deal_owners.full_name where
 *      is_primary = true).
 *   3. Synthetic fallback: "Deal YYYY-MM-DD <short-id>" so the deal is
 *      at least scannable in a list view.
 *
 * Never blocks phase transition; all errors are non-fatal. Writes both
 * `name` and `display_name` to keep cockpit/list-view consistent — see
 * the existing `namingSyncGuard` for why this matters.
 */

import "server-only";

import { supabaseAdmin } from "@/lib/supabase/admin";
import { writeEvent } from "@/lib/ledger/writeEvent";
import { isAutoGeneratedDealName } from "@/lib/deals/isAutoGeneratedDealName";

export type RepairBlankDealNameInput = {
  dealId: string;
  bankId?: string | null;
};

export type RepairBlankDealNameResult = {
  repaired: boolean;
  /** The name that ended up on the deal. Null if we left it alone. */
  finalName: string | null;
  /** Which source produced the name we wrote. */
  source: "no_repair_needed" | "borrower_name" | "primary_owner" | "fallback";
};

const UNTITLED_PATTERN = /^untitled\s+deal/i;

function isBlankOrPlaceholder(name: string | null | undefined): boolean {
  if (!name) return true;
  const trimmed = name.trim();
  if (trimmed.length === 0) return true;
  if (trimmed === "NEEDS NAME") return true;
  if (UNTITLED_PATTERN.test(trimmed)) return true;
  if (isAutoGeneratedDealName(trimmed)) return true;
  return false;
}

function formatFallback(dealId: string, now: Date): string {
  const y = now.getUTCFullYear();
  const m = String(now.getUTCMonth() + 1).padStart(2, "0");
  const d = String(now.getUTCDate()).padStart(2, "0");
  const shortId = dealId.slice(0, 8);
  return `Deal ${y}-${m}-${d} ${shortId}`;
}

export async function repairBlankDealName(
  input: RepairBlankDealNameInput,
): Promise<RepairBlankDealNameResult> {
  const sb = supabaseAdmin();
  const { dealId } = input;

  const { data: deal, error: dealErr } = await sb
    .from("deals")
    .select("name, display_name, borrower_name")
    .eq("id", dealId)
    .maybeSingle();

  if (dealErr || !deal) {
    return { repaired: false, finalName: null, source: "no_repair_needed" };
  }

  const current = (deal as { name: string | null }).name ?? null;
  if (!isBlankOrPlaceholder(current)) {
    return { repaired: false, finalName: current, source: "no_repair_needed" };
  }

  // Source 1: borrower_name on the deal row.
  const borrowerName = (deal as { borrower_name: string | null }).borrower_name;
  let candidate: string | null = null;
  let source: RepairBlankDealNameResult["source"] = "fallback";
  if (borrowerName && !isBlankOrPlaceholder(borrowerName)) {
    candidate = borrowerName.trim();
    source = "borrower_name";
  }

  // Source 2: primary owner's full_name (only if borrower_name was unusable).
  if (!candidate) {
    const { data: owner } = await sb
      .from("deal_owners")
      .select("full_name")
      .eq("deal_id", dealId)
      .eq("is_primary", true)
      .maybeSingle();
    const ownerName = (owner as { full_name: string | null } | null)
      ?.full_name;
    if (ownerName && !isBlankOrPlaceholder(ownerName)) {
      candidate = ownerName.trim();
      source = "primary_owner";
    }
  }

  // Source 3: synthetic fallback.
  if (!candidate) {
    candidate = formatFallback(dealId, new Date());
    source = "fallback";
  }

  const { error: updateErr } = await sb
    .from("deals")
    .update({
      name: candidate,
      display_name: candidate,
      naming_method: source === "fallback" ? "fallback" : "derived",
      naming_fallback_reason:
        source === "fallback"
          ? "no_borrower_name_or_primary_owner_available"
          : null,
      named_at: new Date().toISOString(),
    })
    .eq("id", dealId);

  if (updateErr) {
    void writeEvent({
      dealId,
      kind: "intake.deal_name_repair_failed",
      scope: "intake",
      meta: {
        attempted_name: candidate,
        source,
        error: updateErr.message,
      },
    });
    return { repaired: false, finalName: current, source: "no_repair_needed" };
  }

  void writeEvent({
    dealId,
    kind: "intake.deal_name_repaired",
    scope: "intake",
    meta: {
      previous_name: current,
      new_name: candidate,
      source,
    },
  });

  return { repaired: true, finalName: candidate, source };
}
