function drawMetadataGrid(s: DocState) {
  const { doc, periods } = s;
  const metaRows = ["Statement Date", "Months Covered", "Audit Method", "Stmt Type"];
  const metaFns: ((p: StatementPeriod) => string)[] = [
    (p) => p.date,
    (p) => String(p.months),
    (p) => p.auditMethod,
    (p) => p.stmtType,
  ];

  doc.fontSize(FONT_SIZE_META);

  for (let r = 0; r < metaRows.length; r++) {
    const rowY = s.y;

    // Label cell (gray bg)
    doc.rect(PAGE_MARGIN, rowY, COL_WIDTH_LABEL, ROW_HEIGHT).fill("#f0f0f0");
    doc.fillColor("#000000").font(FONT_BOLD).fontSize(FONT_SIZE_META);
    doc.text(metaRows[r]!, PAGE_MARGIN + 4, rowY + 2, { width: COL_WIDTH_LABEL - 8 });

    // Value cells
    doc.font(FONT_NORMAL);
    let x = PAGE_MARGIN + COL_WIDTH_LABEL;
    for (let c = 0; c < periods.length; c++) {
      const colW = s.showPctColumns ? COL_WIDTH_VALUE + COL_WIDTH_PCT : COL_WIDTH_VALUE;
      const val = metaFns[r]!(periods[c]!);
      doc.text(val, x + 2, rowY + 2, { width: colW - 4, align: "center" });
      x += colW;
    }

    // Light line
    doc.moveTo(PAGE_MARGIN, rowY + ROW_HEIGHT)
      .lineTo(x, rowY + ROW_HEIGHT)
      .lineWidth(0.2).stroke("#cccccc");

    s.y += ROW_HEIGHT;
  }

  s.y += 4; // gap after metadata
}

