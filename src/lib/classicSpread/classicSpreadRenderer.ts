function fmtCurrency(val: number | null): string {
  if (val == null) return "\u2014";
  if (val < 0) return `($${Math.abs(val).toLocaleString("en-US", { maximumFractionDigits: 0 })})`;
  return `$${val.toLocaleString("en-US", { maximumFractionDigits: 0 })}`;
}

function renderGlobalCashFlowPage(
  doc: PDFKit.PDFDocument,
  input: ClassicSpreadInput,
  gcf: GlobalCashFlowSection,
  pageNum: number,
): number {
  const rightEdge = doc.page.width - PAGE_MARGIN;
  const contentWidth = rightEdge - PAGE_MARGIN;
  const labelWidth = 300;
  const valueX = rightEdge - 120;
  const valueWidth = 120;
  let y = PAGE_MARGIN;

  // ── Header bar ──────────────────────────────────────────────────────────
  doc.font(FONT_BOLD).fontSize(FONT_SIZE_TITLE);
  doc.text(input.companyName.toUpperCase(), PAGE_MARGIN, y, { width: contentWidth });

  doc.font(FONT_NORMAL).fontSize(FONT_SIZE_HEADER);
  doc.text("Global Cash Flow Analysis", PAGE_MARGIN, y + 14, { width: 300 });
  doc.text("Generated by Buddy", rightEdge - 150, y + 14, { width: 150, align: "right" });

  doc.fontSize(FONT_SIZE_META);
  if (gcf.taxYear) {
    doc.text(`Tax Year: ${gcf.taxYear}`, PAGE_MARGIN, y + 28, { width: 350 });
  }
  doc.text(input.preparedDate, rightEdge - 150, y + 28, { width: 150, align: "right" });

  const ruleY = y + HEADER_HEIGHT - 4;
  doc.moveTo(PAGE_MARGIN, ruleY).lineTo(rightEdge, ruleY).lineWidth(0.5).stroke("#333333");
  y = ruleY + 12;

  // ── Section: Entity Cash Flow ───────────────────────────────────────────
  doc.font(FONT_BOLD).fontSize(FONT_SIZE_HEADER).fillColor("#333333");
  doc.text("ENTITY CASH FLOW", PAGE_MARGIN, y, { width: contentWidth });
  y += 14;

  doc.moveTo(PAGE_MARGIN, y).lineTo(rightEdge, y).lineWidth(0.3).stroke("#cccccc");
  y += 4;

  if (gcf.entityCashFlowAvailable != null) {
    doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY).fillColor("#000000");
    doc.text("Entity Cash Flow Available", PAGE_MARGIN + 8, y + 2, { width: labelWidth });
    doc.font(FONT_BOLD);
    doc.text(fmtCurrency(gcf.entityCashFlowAvailable), valueX, y + 2, { width: valueWidth, align: "right" });
    y += ROW_HEIGHT;

    doc.font(FONT_NORMAL).fontSize(FONT_SIZE_META).fillColor("#666666");
    doc.text(`(from ${gcf.entityCount} operating ${gcf.entityCount === 1 ? "entity" : "entities"})`, PAGE_MARGIN + 20, y + 1, { width: labelWidth });
    doc.fillColor("#000000");
    y += ROW_HEIGHT;
  } else {
    doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY).fillColor("#999999");
    doc.text("Entity data not yet computed \u2014 re-run spread pipeline", PAGE_MARGIN + 8, y + 2, { width: contentWidth });
    doc.fillColor("#000000");
    y += ROW_HEIGHT + 4;
  }

  y += 8;

  // ── Section: Sponsor Contributions ──────────────────────────────────────
  doc.font(FONT_BOLD).fontSize(FONT_SIZE_HEADER).fillColor("#333333");
  doc.text("SPONSOR CONTRIBUTIONS", PAGE_MARGIN, y, { width: contentWidth });
  y += 14;

  doc.moveTo(PAGE_MARGIN, y).lineTo(rightEdge, y).lineWidth(0.3).stroke("#cccccc");
  y += 4;

  if (gcf.sponsors.length > 0) {
    for (const sponsor of gcf.sponsors) {
      doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY).fillColor("#000000");
      doc.text(sponsor.displayName, PAGE_MARGIN + 8, y + 2, { width: labelWidth });
      doc.text(fmtCurrency(sponsor.personalCashAvailable), valueX, y + 2, { width: valueWidth, align: "right" });
      y += ROW_HEIGHT;
    }
  } else {
    doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY).fillColor("#999999");
    doc.text("No personal financial data \u2014 re-extract personal tax returns", PAGE_MARGIN + 8, y + 2, { width: contentWidth });
    doc.fillColor("#000000");
    y += ROW_HEIGHT + 4;
  }

  y += 12;

  // ── Divider ─────────────────────────────────────────────────────────────
  doc.moveTo(PAGE_MARGIN, y).lineTo(rightEdge, y).lineWidth(0.8).stroke("#333333");
  y += 12;

  // ── Section: Global Summary ─────────────────────────────────────────────
  doc.font(FONT_BOLD).fontSize(FONT_SIZE_HEADER).fillColor("#333333");
  doc.text("GLOBAL SUMMARY", PAGE_MARGIN, y, { width: contentWidth });
  y += 14;

  doc.moveTo(PAGE_MARGIN, y).lineTo(rightEdge, y).lineWidth(0.3).stroke("#cccccc");
  y += 4;

  // Global Cash Flow Available (bold)
  doc.font(FONT_BOLD).fontSize(FONT_SIZE_BODY).fillColor("#000000");
  doc.text("Global Cash Flow Available", PAGE_MARGIN + 8, y + 2, { width: labelWidth });
  doc.text(fmtCurrency(gcf.globalCashFlow), valueX, y + 2, { width: valueWidth, align: "right" });
  y += ROW_HEIGHT;

  // Proposed Annual Debt Service
  doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY);
  doc.text("Proposed Annual Debt Service", PAGE_MARGIN + 8, y + 2, { width: labelWidth });
  const dsDisplay = gcf.proposedAnnualDebtService != null
    ? `(${fmtCurrency(gcf.proposedAnnualDebtService).replace(/^\$/, "$")})`
    : "\u2014";
  doc.text(dsDisplay, valueX, y + 2, { width: valueWidth, align: "right" });
  y += ROW_HEIGHT;

  // Separator line
  doc.moveTo(PAGE_MARGIN + 8, y).lineTo(rightEdge, y).lineWidth(0.3).stroke("#cccccc");
  y += 6;

  // Global DSCR (large, colored)
  doc.font(FONT_BOLD).fontSize(12);
  doc.text("Global DSCR", PAGE_MARGIN + 8, y + 2, { width: labelWidth });

  const dscrDisplay = gcf.globalDscr != null ? `${gcf.globalDscr.toFixed(2)}x` : "\u2014";
  const dscrColor = gcf.coverageStatus === "ADEQUATE" ? "#16a34a"
    : gcf.coverageStatus === "TIGHT" ? "#d97706"
    : gcf.coverageStatus === "DEFICIT" ? "#dc2626"
    : "#666666";
  doc.fillColor(dscrColor);
  doc.text(dscrDisplay, valueX, y + 2, { width: valueWidth, align: "right" });
  doc.fillColor("#000000");
  y += 20;

  // ── Coverage Signal Bar ─────────────────────────────────────────────────
  const barHeight = 28;
  const accentColor = gcf.coverageStatus === "ADEQUATE" ? "#22c55e"
    : gcf.coverageStatus === "TIGHT" ? "#f59e0b"
    : gcf.coverageStatus === "DEFICIT" ? "#ef4444"
    : "#999999";

  // Left accent border
  doc.rect(PAGE_MARGIN, y, 4, barHeight).fill(accentColor);
  // Light background
  doc.rect(PAGE_MARGIN + 4, y, contentWidth - 4, barHeight).fill("#f9fafb");

  const statusLabel = gcf.coverageStatus === "ADEQUATE" ? "ADEQUATE \u2014 DSCR \u2265 1.25x"
    : gcf.coverageStatus === "TIGHT" ? "TIGHT \u2014 DSCR 1.00x\u20131.25x"
    : gcf.coverageStatus === "DEFICIT" ? "DEFICIT \u2014 DSCR < 1.00x"
    : "UNKNOWN \u2014 Insufficient data to compute DSCR";

  doc.font(FONT_BOLD).fontSize(FONT_SIZE_BODY).fillColor(accentColor);
  doc.text(statusLabel, PAGE_MARGIN + 14, y + 9, { width: contentWidth - 24, lineBreak: false });
  doc.fillColor("#000000");
  y += barHeight + 16;

  // ── Footer ──────────────────────────────────────────────────────────────
  const footerTop = doc.page.height - PAGE_MARGIN - FOOTER_HEIGHT + 8;

  doc.moveTo(PAGE_MARGIN, footerTop).lineTo(rightEdge, footerTop).lineWidth(0.3).stroke("#999999");

  doc.font(FONT_NORMAL).fontSize(FONT_SIZE_META).fillColor("#666666");
  doc.text(`Page ${pageNum}`, PAGE_MARGIN, footerTop + 6, { width: 100, lineBreak: false });
  doc.text("Buddy The Underwriter", rightEdge - 160, footerTop + 6, { width: 160, align: "right", lineBreak: false });

  doc.text(
    "Global cash flow includes ownership-adjusted entity cash flow plus guarantor personal income net of personal obligations and living expenses.",
    PAGE_MARGIN,
    footerTop + 16,
    { width: contentWidth, lineBreak: false },
  );
  doc.text("Confidential \u2014 For Internal Use Only", PAGE_MARGIN, footerTop + 26, { width: contentWidth, align: "center", lineBreak: false });

  doc.fillColor("#000000");

  return pageNum;
}

