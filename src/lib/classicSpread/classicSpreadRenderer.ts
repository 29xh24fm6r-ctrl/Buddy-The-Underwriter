// ---------------------------------------------------------------------------
// Narrative Section Rendering
// ---------------------------------------------------------------------------

function drawNarrativeSections(s: DocState, narrative: SpreadNarrative) {
  const { doc } = s;
  const rightEdge = doc.page.width - PAGE_MARGIN;
  const textWidth = rightEdge - PAGE_MARGIN;

  for (const section of narrative.sections) {
    checkPageBreak(s, 6);

    // Section title
    doc.font(FONT_BOLD).fontSize(FONT_SIZE_HEADER).fillColor("#333333");
    doc.text(section.title, PAGE_MARGIN, s.y, { width: textWidth });
    s.y += 14;

    // Section body
    doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY).fillColor("#000000");
    const bodyHeight = doc.heightOfString(section.body, { width: textWidth });
    doc.text(section.body, PAGE_MARGIN, s.y, { width: textWidth });
    s.y += bodyHeight + 12;
  }

  // Attribution
  doc.font(FONT_NORMAL).fontSize(FONT_SIZE_META).fillColor("#999999");
  doc.text(
    `Generated by ${narrative.model} at ${new Date(narrative.generatedAt).toLocaleString("en-US")}`,
    PAGE_MARGIN,
    s.y,
    { width: textWidth, align: "right", lineBreak: false },
  );
  doc.fillColor("#000000");
  s.y += 12;
}

// ---------------------------------------------------------------------------
// Ratio Section Rendering
// ---------------------------------------------------------------------------

function drawRatioSections(s: DocState, sections: RatioSection[]) {
  const { doc, periods } = s;

  for (const section of sections) {
    checkPageBreak(s, 2);

    // Section title: gray band
    const rightEdge = PAGE_MARGIN + COL_WIDTH_LABEL + periods.length * COL_WIDTH_VALUE;
    doc.rect(PAGE_MARGIN, s.y, rightEdge - PAGE_MARGIN, ROW_HEIGHT).fill("#f0f0f0");
    doc.fillColor("#000000");
    doc.font(FONT_BOLD).fontSize(FONT_SIZE_BODY);
    doc.text(section.title, PAGE_MARGIN + 4, s.y + 2, { width: COL_WIDTH_LABEL - 8 });
    s.y += ROW_HEIGHT;

    for (const row of section.rows) {
      checkPageBreak(s);

      doc.font(FONT_NORMAL).fontSize(FONT_SIZE_BODY);
      doc.text(row.label, PAGE_MARGIN + 16, s.y + 2, { width: COL_WIDTH_LABEL - 20 });

      let x = PAGE_MARGIN + COL_WIDTH_LABEL;
      for (let i = 0; i < periods.length; i++) {
        const val = row.values[i] ?? null;
        const display = fmtRatioValue(val, row.format, row.decimals);
        doc.text(display, x, s.y + 2, { width: COL_WIDTH_VALUE - 4, align: "right" });
        x += COL_WIDTH_VALUE;
      }

      doc.moveTo(PAGE_MARGIN, s.y + ROW_HEIGHT)
        .lineTo(x, s.y + ROW_HEIGHT)
        .lineWidth(0.15).stroke("#dddddd");

      s.y += ROW_HEIGHT;
    }

    s.y += 6; // gap between sections
  }
}

