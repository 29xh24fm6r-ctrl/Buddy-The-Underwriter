/**
 * Surface Wiring Ledger — Phase 64
 *
 * Canonical source of truth for every Stitch surface's integration status.
 *
 * Status model (Phase 64 canonical):
 * - recovery_optional          — optional recovery route, not production-critical
 * - visual_static              — static Stitch HTML only, no Buddy activation
 * - partial_readonly           — some real data injected, no write actions
 * - partial_interactive        — some real data + some write actions
 * - wired_readonly             — fully bound to real Buddy data, no writes
 * - wired_interactive          — fully bound to real Buddy data + real write actions
 * - blocked_auth               — route exists but auth/permission blocks activation
 * - blocked_missing_backend    — no backend APIs exist for this surface's domain
 * - blocked_missing_data_contract — backend exists but no data contract defined
 * - retired                    — surface removed or replaced
 */

export type WiringStatus =
  | "recovery_optional"
  | "visual_static"
  | "partial_readonly"
  | "partial_interactive"
  | "wired_readonly"
  | "wired_interactive"
  | "blocked_auth"
  | "blocked_missing_backend"
  | "blocked_missing_data_contract"
  | "retired";

