/** Lookup by surface key */
export function getWiringEntry(key: string): SurfaceWiringEntry | undefined {
  return SURFACE_WIRING_LEDGER.find((e) => e.key === key);
}

/** Summary counts */
export function getWiringSummary() {
  const total = SURFACE_WIRING_LEDGER.length;
  const counts: Record<WiringStatus, number> = {
    recovery_optional: 0,
    visual_static: 0,
    partial_readonly: 0,
    partial_interactive: 0,
    wired_readonly: 0,
    wired_interactive: 0,
    blocked_auth: 0,
    blocked_missing_backend: 0,
    blocked_missing_data_contract: 0,
    retired: 0,
  };

  for (const entry of SURFACE_WIRING_LEDGER) {
    counts[entry.status]++;
  }

  const required = SURFACE_WIRING_LEDGER.filter((e) => e.required).length;
  const withActivation = SURFACE_WIRING_LEDGER.filter((e) => e.hasActivationScript).length;

  return { total, required, withActivation, ...counts };
}
