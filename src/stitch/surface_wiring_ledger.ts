/** Lookup by surface key */
export function getWiringEntry(key: string): SurfaceWiringEntry | undefined {
  return SURFACE_WIRING_LEDGER.find((e) => e.key === key);
}

/** Summary counts */
export function getWiringSummary() {
  const total = SURFACE_WIRING_LEDGER.length;
  const wired = SURFACE_WIRING_LEDGER.filter((e) => e.status === "wired").length;
  const visual = SURFACE_WIRING_LEDGER.filter((e) => e.status === "visual").length;
  const partial = SURFACE_WIRING_LEDGER.filter((e) => e.status === "partial").length;
  const broken = SURFACE_WIRING_LEDGER.filter((e) => e.status === "broken").length;
  const unverified = SURFACE_WIRING_LEDGER.filter((e) => e.status === "unverified").length;
  const required = SURFACE_WIRING_LEDGER.filter((e) => e.required).length;
  const withActivation = SURFACE_WIRING_LEDGER.filter((e) => e.hasActivationScript).length;

  return { total, wired, visual, partial, broken, unverified, required, withActivation };
}
