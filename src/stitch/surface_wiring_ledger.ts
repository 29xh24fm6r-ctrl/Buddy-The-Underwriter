/**
 * Surface Wiring Ledger — Phase 62B
 *
 * Canonical source of truth for every Stitch surface's integration status.
 * Each entry describes what the surface needs to be fully wired to Buddy.
 *
 * Status values:
 * - "wired"      — loads real Buddy data via activation script
 * - "visual"     — renders saved Stitch design, no live data injection yet
 * - "partial"    — has activation script but missing some data bindings
 * - "broken"     — route exists but rendering or data flow fails
 * - "unverified" — not yet inspected
 */

export type WiringStatus = "wired" | "visual" | "partial" | "broken" | "unverified";

export type IntegrationType =
  | "read_only"       // displays data, no user actions
  | "read_nav"        // displays data + navigates to other routes
  | "read_write"      // displays data + performs write actions
  | "token_activated" // externalized view activated by borrower token
  | "visual_only";    // static Stitch design, no data integration

